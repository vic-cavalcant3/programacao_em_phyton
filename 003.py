#Escreva um programa que leia o nome, e o sobrenome, CONCATENE em uma nova variável nome completo, e retorne para o
# usuário

#Leitura dos dados
nome = input('Digite seu nome: ')
sobrenome = input('Digite seu sobrenome: ')

#Fazendo a Concatene
resultado = nome + ' ' + sobrenome

#Resultado da Conta
print(f'O seu nome e sobrenome é {resultado}')